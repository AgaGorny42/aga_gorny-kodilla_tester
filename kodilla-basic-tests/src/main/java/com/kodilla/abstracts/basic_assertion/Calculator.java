package com.kodilla.abstracts.basic_assertion;

public class Calculator {

    int a;
    int b;

    public int sum(int a, int b){
        return a + b;

    }
    public int subtract(int a, int b) {
        return a - b;
    }

    public double pow(int a, int b) {
        return Math.pow(a, b);
    }
}
